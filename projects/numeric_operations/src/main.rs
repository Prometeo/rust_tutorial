fn main() {
    // adition
    let sum = 5 + 10;
    println!("5 + 10: {}", sum);

    // substraccion
    let difference = 95.5 - 4.3;
    println!("95.5 - 4.3: {}", difference);

    // multiplication
    let product = 4 * 30;
    println!("4 * 30: {}", product);

    //division
    let quotient = 56.7 / 32.2;
    println!("56.7 / 32.2: {}", quotient);

    //remainder
    let remainder = 43 % 5;
    println!("43 % 5: {}", remainder);
}
